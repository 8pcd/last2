package academy.everyonecodes.java.race;

public class RaceCar {
	private int speed;
	private int damage;
	
	RaceCar(int speed, int damage){
	 this.speed = speed;
	    this.damage = damage;
	
	}
	public int getSpeed() {
	    return speed;
	}

	public void setSpeed(int speed) {
	    this.speed = speed;
	}

	public int getDamage() {
	    return damage;
	}

	public void setDamage(int damage) {
	    this.damage = damage;

}
}
